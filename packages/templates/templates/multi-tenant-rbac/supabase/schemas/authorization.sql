-- Multi-tenant role-based access control.
-- Add organization_id to tenant-owned tables and use authorize() in RLS policies.

create type public.app_role as enum ('owner', 'admin', 'member');

create type public.app_permission as enum (
  'organizations.read',
  'organizations.update',
  'organizations.delete',
  'members.read',
  'members.invite',
  'members.update',
  'members.remove',
  'projects.read',
  'projects.create',
  'projects.update',
  'projects.delete'
);

create table public.organizations (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  slug text not null unique,
  created_at timestamptz default now()
);

create table public.organization_members (
  id bigint generated by default as identity primary key,
  organization_id uuid references public.organizations on delete cascade not null,
  user_id uuid references auth.users on delete cascade not null,
  role public.app_role not null default 'member',
  created_at timestamptz default now(),
  unique (organization_id, user_id)
);

comment on table public.organization_members is
  'Organization membership and role assignment for each user.';

create index organization_members_user_id_idx
on public.organization_members (user_id);

create table public.role_permissions (
  id bigint generated by default as identity primary key,
  role public.app_role not null,
  permission public.app_permission not null,
  unique (role, permission)
);

comment on table public.role_permissions is
  'Permissions granted to each organization role.';

create or replace function public.current_organization_ids()
returns setof uuid
language sql
stable
security definer
set search_path = ''
as $$
  select organization_id
  from public.organization_members
  where user_id = auth.uid();
$$;

create or replace function public.authorize(
  requested_organization_id uuid,
  requested_permission public.app_permission
)
returns boolean
language sql
stable
security definer
set search_path = ''
as $$
  select exists (
    select 1
    from public.organization_members as organization_members
    join public.role_permissions as role_permissions
      on role_permissions.role = organization_members.role
    where organization_members.organization_id = requested_organization_id
      and organization_members.user_id = auth.uid()
      and role_permissions.permission = requested_permission
  );
$$;

create or replace function public.create_organization(
  organization_name text,
  organization_slug text
)
returns public.organizations
language plpgsql
security definer
set search_path = ''
as $$
declare
  new_organization public.organizations;
begin
  if auth.uid() is null then
    raise exception 'Authentication is required';
  end if;

  insert into public.organizations (name, slug)
  values (organization_name, organization_slug)
  returning * into new_organization;

  insert into public.organization_members (organization_id, user_id, role)
  values (new_organization.id, auth.uid(), 'owner');

  return new_organization;
end;
$$;

revoke execute on function public.create_organization(text, text)
from public;

grant execute on function public.create_organization(text, text)
to authenticated;

alter table public.organizations enable row level security;
alter table public.organization_members enable row level security;
alter table public.role_permissions enable row level security;

create policy "Members can read organizations"
on public.organizations
for select
to authenticated
using ((select public.authorize(id, 'organizations.read')));

create policy "Authorized members can update organizations"
on public.organizations
for update
to authenticated
using ((select public.authorize(id, 'organizations.update')))
with check ((select public.authorize(id, 'organizations.update')));

create policy "Authorized members can delete organizations"
on public.organizations
for delete
to authenticated
using ((select public.authorize(id, 'organizations.delete')));

create policy "Members can read organization memberships"
on public.organization_members
for select
to authenticated
using (
  user_id = auth.uid()
  or (select public.authorize(organization_id, 'members.read'))
);

create policy "Authorized members can invite organization members"
on public.organization_members
for insert
to authenticated
with check (
  (
    role = 'member'
    and (select public.authorize(organization_id, 'members.invite'))
  )
  or (select public.authorize(organization_id, 'members.update'))
);

create policy "Authorized members can update organization members"
on public.organization_members
for update
to authenticated
using ((select public.authorize(organization_id, 'members.update')))
with check ((select public.authorize(organization_id, 'members.update')));

create policy "Authorized members can remove organization members"
on public.organization_members
for delete
to authenticated
using ((select public.authorize(organization_id, 'members.remove')));

create policy "Authenticated users can read role permissions"
on public.role_permissions
for select
to authenticated
using (true);

-- Example tenant-scoped table.
create table public.projects (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid references public.organizations on delete cascade not null,
  name text not null,
  created_at timestamptz default now()
);

create index projects_organization_id_idx
on public.projects (organization_id);

alter table public.projects enable row level security;

