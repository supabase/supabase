function generateRadixVariables() {
  const colors = [
    'brand',
    'brand-fixed',
    'scale',
    'tomato',
    'red',
    'crimson',
    'pink',
    'plum',
    'purple',
    'violet',
    'indigo',
    'blue',
    'cyan',
    'teal',
    'green',
    'grass',
    'brown',
    'orange',
    'sky',
    'mint',
    'lime',
    'yellow',
    'amber',
    'gold',
    'bronze',
    'gray',
    'mauve',
    'slate',
    'sage',
    'olive',
    'sand',
  ]

  let mappedColors = {}

  colors.map((x) => {
    mappedColors[x] = {}
  })

  colors.map((x) => {
    for (let index = 0; index < 12; index++) {
      const step = index + 1
      mappedColors[x][step * 100] = `var(--colors-${x}${step})`
    }
  })

  return mappedColors
}

