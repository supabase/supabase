#!/usr/bin/env node
/**
 * Emits packages/config/css/radix-vars.css from @radix-ui/colors and the
 * existing default-colors.js scale aliases. Run from packages/config/.
 *
 * One-shot generator — committed alongside the CSS output so future updates
 * (new Radix versions, new aliases) can be reproduced without searching for
 * how the file was made.
 */

const fs = require('node:fs')
const path = require('node:path')

const radixUiColors = require('@radix-ui/colors')

const excludedRadixColors = [
  'bronze',
  'brown',
  'cyan',
  'grass',
  'olive',
  'mauve',
  'mint',
  'lime',
  'plum',
  'sage',
  'sand',
  'sky',
  'teal',
]

const isExcluded = (key) => excludedRadixColors.some((ex) => key.startsWith(ex))

const lightEntries = []
const darkEntries = []

for (const [key, palette] of Object.entries(radixUiColors)) {
  if (isExcluded(key)) continue
  const target = key.includes('Dark') ? darkEntries : lightEntries
  for (const [varName, value] of Object.entries(palette)) {
    target.push([varName, value])
  }
}

const formatBlock = (selector, entries) => {
  const lines = entries.map(([k, v]) => `  --colors-${k}: ${v};`).join('\n')
  return `${selector} {\n${lines}\n}`
}

const scaleAliasesLight = []
const scaleAliasesDark = []
for (let i = 1; i <= 12; i++) {
  scaleAliasesLight.push([`scale${i}`, `var(--colors-slate${i})`])
  scaleAliasesLight.push([`scaleA${i}`, `var(--colors-slateA${i})`])
  if (i === 11) {
    // Preserves the hard-coded value from default-colors.js scaleDark.scale11.
    scaleAliasesDark.push([`scale${i}`, `#bbbbbb`])
  } else {
    scaleAliasesDark.push([`scale${i}`, `var(--colors-gray${i})`])
  }
  scaleAliasesDark.push([`scaleA${i}`, `var(--colors-grayA${i})`])
}

const header = `/*
 * Auto-generated by scripts/generate-radix-vars.js — do not edit by hand.
 * Run \`node scripts/generate-radix-vars.js\` from packages/config/ to refresh.
 *
 * Mirrors the CSS variables that the legacy @mertasan/tailwindcss-variables
 * plugin used to emit from ui.config.js's generateCssVariables() and
 * default-colors.js's scale/scaleA aliases.
 */
`

const output = [
  header,
  formatBlock(':root', [...lightEntries, ...scaleAliasesLight]),
  '',
  formatBlock("[data-theme*='dark']", [...darkEntries, ...scaleAliasesDark]),
  '',
].join('\n')

const outPath = path.resolve(__dirname, '..', 'css', 'radix-vars.css')
fs.writeFileSync(outPath, output)
console.log(`wrote ${outPath} (${output.split('\n').length} lines)`)
