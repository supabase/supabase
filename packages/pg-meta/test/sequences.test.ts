withTestDatabase('list with filters', async ({ executeQuery }) => {
  // includeSystemSchemas
  const { sql: withSystemSql, zod } = pgMeta.sequences.list({ includeSystemSchemas: true })
  const withSystem = zod.parse(await executeQuery(withSystemSql))
  expect(withSystem.length).toBeGreaterThan(0)

  // default excludes system schemas
  const { sql: withoutSystemSql, zod: withoutSystemZod } = pgMeta.sequences.list()
  const withoutSystem = withoutSystemZod.parse(await executeQuery(withoutSystemSql))
  expect(withoutSystem.every((s) => s.schema === 'public')).toBe(true)

  // includedSchemas
  const { sql: includedSql, zod: includedZod } = pgMeta.sequences.list({
    includedSchemas: ['public'],
  })
  const included = includedZod.parse(await executeQuery(includedSql))
  expect(included.every((s) => s.schema === 'public')).toBe(true)

  // excludedSchemas
  const { sql: excludedSql, zod: excludedZod } = pgMeta.sequences.list({
    excludedSchemas: ['public'],
  })
  const excluded = excludedZod.parse(await executeQuery(excludedSql))
  expect(excluded.some((s) => s.schema === 'public')).toBe(false)

  // limit
  const { sql: limitSql, zod: limitZod } = pgMeta.sequences.list({ limit: 1 })
  const limited = limitZod.parse(await executeQuery(limitSql))
  expect(limited).toHaveLength(1)
})
