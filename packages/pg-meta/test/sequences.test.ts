import { afterAll, beforeAll, expect, test } from 'vitest'

import pgMeta from '../src/index'
import { cleanupRoot, createTestDatabase } from './db/utils'

beforeAll(async () => {
  // Any global setup if needed
})

afterAll(async () => {
  await cleanupRoot()
})

const withTestDatabase = (
  name: string,
  fn: (db: Awaited<ReturnType<typeof createTestDatabase>>) => Promise<void>
) => {
  test(name, async () => {
    const db = await createTestDatabase()
    try {
      await fn(db)
    } finally {
      await db.cleanup()
    }
  })
}

withTestDatabase('list sequences', async ({ executeQuery }) => {
  const { sql, zod } = pgMeta.sequences.list()
  const sequences = zod.parse(await executeQuery(sql))

  // users and todos tables have GENERATED BY DEFAULT AS IDENTITY columns
  const usersSeq = sequences.find((s) => s.name === 'users_id_seq')!
  expect(usersSeq).toBeDefined()
  expect(usersSeq.schema).toBe('public')
  expect(usersSeq.data_type).toBe('bigint')
  expect(usersSeq.start_value).toBe(1)
  expect(usersSeq.increment_by).toBe(1)
  expect(usersSeq.cycle).toBe(false)
})

withTestDatabase('retrieve sequence', async ({ executeQuery }) => {
  const { sql: listSql, zod: listZod } = pgMeta.sequences.list()
  const sequences = listZod.parse(await executeQuery(listSql))
  const usersSeq = sequences.find((s) => s.name === 'users_id_seq')!
  expect(usersSeq).toBeDefined()

  const { sql: retrieveSql, zod: retrieveZod } = pgMeta.sequences.retrieve({ id: usersSeq.id })
  const retrieved = retrieveZod.parse((await executeQuery(retrieveSql))[0])
  expect(retrieved).toBeDefined()
  expect(retrieved!.id).toBe(usersSeq.id)
  expect(retrieved!.name).toBe('users_id_seq')
  expect(retrieved!.schema).toBe('public')
})

