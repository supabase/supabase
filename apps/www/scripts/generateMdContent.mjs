// @ts-check

/**
 * Reads all .md files from content/md/ and generates a TypeScript module
 * that exports the content as a Map. This makes the content statically
 * importable and traceable by @vercel/nft for serverless bundling.
 *
 * Slug list is derived by scanning content/md/ so there's no hardcoded
 * list to keep in sync with MD_PAGES in lib/constants.ts.
 *
 * Run as part of content:build before the Next.js build.
 */

import { promises as fs } from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const contentDir = path.join(__dirname, '../content/md')
const outputPath = path.join(__dirname, '../app/api-v2/md/content.generated.ts')

async function collectMdFiles(dir, prefix = '') {
  const results = []
  const dirents = await fs.readdir(dir, { withFileTypes: true })
  for (const dirent of dirents) {
    const slug = prefix ? `${prefix}/${dirent.name}` : dirent.name
    if (dirent.isDirectory()) {
      results.push(...(await collectMdFiles(path.join(dir, dirent.name), slug)))
    } else if (dirent.name.endsWith('.md')) {
      results.push(slug.replace(/\.md$/, ''))
    }
  }
  return results
}

const slugs = (await collectMdFiles(contentDir)).sort()

if (slugs.length === 0) {
  console.error('❌ No .md files found in content/md/')
  process.exit(1)
}

const entries = []
const errors = []
for (const slug of slugs) {
  const filePath = path.join(contentDir, `${slug}.md`)
  try {
    const content = await fs.readFile(filePath, 'utf-8')
    entries.push(`  [${JSON.stringify(slug)}, ${JSON.stringify(content)}]`)
  } catch (err) {
    errors.push(`${slug}.md: ${err.message}`)
  }
}

if (errors.length > 0) {
  console.error('❌ Failed to read .md files:')
  errors.forEach((e) => console.error(`   ${e}`))
  process.exit(1)
}

const output = `// AUTO-GENERATED by scripts/generateMdContent.mjs — do not edit
export const MD_CONTENT = new Map<string, string>([
${entries.join(',\n')},
])
`

await fs.writeFile(outputPath, output, 'utf-8')
console.log(`✅ Generated ${outputPath} (${entries.length} files)`)
