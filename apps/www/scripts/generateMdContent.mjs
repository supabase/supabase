// @ts-check

/**
 * Reads all .md files from content/md/ and generates a TypeScript module
 * that exports the content as a Map. This makes the content statically
 * importable and traceable by @vercel/nft for serverless bundling.
 *
 * Run as part of content:build before the Next.js build.
 */

import { promises as fs } from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const contentDir = path.join(__dirname, '../content/md')
const outputPath = path.join(__dirname, '../app/api-v2/md/content.generated.ts')

const slugs = [
  'homepage',
  'pricing',
  'auth',
  'database',
  'edge-functions',
  'realtime',
  'storage',
  'vector',
  'modules/cron',
  'modules/queues',
]

const entries = []
for (const slug of slugs) {
  const filePath = path.join(contentDir, `${slug}.md`)
  try {
    const content = await fs.readFile(filePath, 'utf-8')
    entries.push(`  [${JSON.stringify(slug)}, ${JSON.stringify(content)}]`)
  } catch {
    console.warn(`⚠️  Skipping ${slug}.md (not found)`)
  }
}

const output = `// AUTO-GENERATED by scripts/generateMdContent.mjs — do not edit
export const MD_CONTENT = new Map<string, string>([
${entries.join(',\n')},
])
`

await fs.writeFile(outputPath, output, 'utf-8')
console.log(`✅ Generated ${outputPath} (${entries.length} files)`)
