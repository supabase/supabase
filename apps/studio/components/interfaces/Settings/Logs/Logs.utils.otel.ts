/**
 * OTEL / ClickHouse query builders for the legacy per-service logs pages.
 *
 * The OTEL endpoint (`logs.all.otel`) stores every log source in a single
 * `logs` table keyed by a `source` column, with the per-log fields the old
 * BigQuery queries unnested from `metadata` exposed flat in the
 * `log_attributes` Map. These builders mirror the BigQuery generators in
 * `Logs.utils.ts` but emit ClickHouse SQL and alias columns to the exact leaf
 * names the existing row renderers read, so the UI needs no changes.
 */

