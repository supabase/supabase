/** Aliased projection column: `log_attributes['<key>'] AS <alias>`. */
const col = (key: string, alias: SafeLogSqlFragment): SafeLogSqlFragment =>
  safeSql`${attr(key)} AS ${alias}`

/**
 * Everything that varies per table when querying the OTEL `logs` table.
 * `columns`/`filterTemplates`/`error`/`warning`/`basePredicate` default to the
 * "nothing extra" case so a plain process-log table is just `{ source }`.
 */
interface OtelSourceDescriptor {
  source: string
  /** Extra projection columns beyond id/timestamp/event_message, aliased to the renderer leaf names. */
  columns?: SafeLogSqlFragment[]
  /** Filter templates for this table; defaults to COMMON_FILTERS. */
  filterTemplates?: Record<string, SqlFilterEntry>
  /** Chart severity predicates; absent => the table has no severity concept. */
  error?: SafeLogSqlFragment
  warning?: SafeLogSqlFragment
  /** Predicate always ANDed with the source filter (e.g. the pg_cron subset). */
  basePredicate?: SafeLogSqlFragment
}

