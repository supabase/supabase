/**
 * OTEL / ClickHouse query builders for the legacy per-service logs pages.
 *
 * The OTEL endpoint (`logs.all.otel`) stores every log source in a single
 * `logs` table keyed by a `source` column, with the per-log fields the old
 * BigQuery queries unnested from `metadata` exposed flat in the `log_attributes`
 * Map. These builders mirror the BigQuery generators in `Logs.utils.ts` but emit
 * ClickHouse SQL and alias columns to the exact leaf names the existing row
 * renderers read, so the UI needs no changes.
 *
 * Everything that varies per table — the `source` value, the projected columns,
 * the chart severity buckets, the filter templates, and any always-on predicate
 * — lives in a single `OTEL_SOURCES` descriptor so each table is described once
 * and coverage can be eyeballed against `LogsTableName`.
 */

const attr = (key: string): SafeLogSqlFragment => safeSql`log_attributes[${lit(key)}]`
const statusAsInt = (key: string): SafeLogSqlFragment => safeSql`toInt32OrZero(${attr(key)})`

// Severity predicates, reused across the chart and (for auth) the filter
// templates so a row buckets identically wherever it is classified. These are
// the OTEL counterparts of the BigQuery getError/getWarningCondition switches
// and the AUTH_LOG_* constants in Logs.constants.ts.
const HTTP_ERROR: SafeLogSqlFragment = safeSql`${statusAsInt('response.status_code')} >= 500`
const HTTP_WARNING: SafeLogSqlFragment = safeSql`${statusAsInt('response.status_code')} BETWEEN 400 AND 499`
const PG_ERROR: SafeLogSqlFragment = safeSql`${attr('parsed.error_severity')} IN ('ERROR', 'FATAL', 'PANIC')`
const PG_WARNING: SafeLogSqlFragment = safeSql`${attr('parsed.error_severity')} = 'WARNING'`
const FN_ERROR: SafeLogSqlFragment = safeSql`${attr('level')} IN ('error', 'fatal')`
const FN_WARNING: SafeLogSqlFragment = safeSql`${attr('level')} = 'warning'`
const MULTIGRES_ERROR: SafeLogSqlFragment = safeSql`JSONExtractString(event_message, 'level') IN ('ERROR', 'FATAL', 'PANIC')`
const MULTIGRES_WARNING: SafeLogSqlFragment = safeSql`JSONExtractString(event_message, 'level') IN ('WARN', 'WARNING')`
const AUTH_ERROR: SafeLogSqlFragment = safeSql`${attr('level')} IN ('error', 'fatal') OR ${statusAsInt('status')} >= 500`
const AUTH_WARNING: SafeLogSqlFragment = safeSql`${attr('level')} = 'warning' OR ${statusAsInt('status')} BETWEEN 400 AND 499`
const AUTH_INFO: SafeLogSqlFragment = safeSql`NOT (${AUTH_ERROR}) AND NOT (${AUTH_WARNING})`
// "no severity concept" — every row counts as ok in the chart.
const NO_MATCH: SafeLogSqlFragment = safeSql`0`

// pg_cron logs are postgres_logs rows generated by the pg_cron extension.
const PG_CRON_PREDICATE: SafeLogSqlFragment = safeSql`(${attr('parsed.application_name')} = 'pg_cron' OR event_message ILIKE '%cron job%')`

// Common filter applied to every table (free-text search over event_message).
const COMMON_FILTERS: Record<string, SqlFilterEntry> = {
  search_query: (value: string) => safeSql`event_message ILIKE ${lit('%' + value + '%')}`,
}
const databaseFilter = (value: string): SafeLogSqlFragment =>
  safeSql`${attr('identifier')} = ${lit(value)}`

// Per-table filter templates: ClickHouse equivalents of SQL_FILTER_TEMPLATES
// (Logs.constants.ts). Tables not listed here use COMMON_FILTERS only.
