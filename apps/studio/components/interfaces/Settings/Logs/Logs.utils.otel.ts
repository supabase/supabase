/**
 * Generic equality resolver for filter keys without a template entry — emits an
 * OTEL `log_attributes[key] = value` predicate (the ClickHouse counterpart of
 * the BigQuery `quotedIdent` fallback). Drops the clause on non-scalar input.
 */
const resolveUnknownOtelClause = (dotKey: string, value: unknown): SafeLogSqlFragment | null => {
  if (typeof value !== 'string' && typeof value !== 'number' && typeof value !== 'boolean') {
    return null
  }
  try {
    return safeSql`${attr(dotKey)} = ${lit(value)}`
  } catch {
    // analyticsLiteral rejects non-finite numbers — drop the clause.
    return null
  }
}

// Builds the full WHERE clause: source predicate (+ any base predicate) AND the
// user's filters, reusing the shared nested AND/OR grouping logic.
const genOtelWhere = (table: LogsTableName, filters: Filters): SafeLogSqlFragment => {
  const desc = OTEL_SOURCES[table]
  const predicates: SafeLogSqlFragment[] = [safeSql`source = ${lit(desc.source)}`]
  if (desc.basePredicate) predicates.push(desc.basePredicate)
  predicates.push(
    ...buildWhereClauses(
      table,
      filters,
      desc.filterTemplates ?? COMMON_FILTERS,
      resolveUnknownOtelClause
    )
  )
  return safeSql`WHERE ${joinSqlFragments(predicates, ' AND ')}`
}

const genOtelSelectColumns = (table: LogsTableName): SafeLogSqlFragment => {
  const columns: SafeLogSqlFragment[] = [
    safeSql`id`,
    safeSql`timestamp`,
    safeSql`event_message`,
    ...(OTEL_SOURCES[table].columns ?? []),
  ]
  return joinSqlFragments(columns, ', ')
}

export const genDefaultQueryOtel = (
  table: LogsTableName,
  filters: Filters,
  limit: number = 100
): SafeLogSqlFragment => {
  return safeSql`-- Logs Preview Query (otel) [${lit(table)}]
SELECT ${genOtelSelectColumns(table)}
FROM logs
${genOtelWhere(table, filters)}
ORDER BY timestamp DESC
LIMIT ${lit(limit)}`
}

export const genCountQueryOtel = (table: LogsTableName, filters: Filters): SafeLogSqlFragment => {
  return safeSql`-- Logs Count Query (otel) [${lit(table)}]
SELECT count() AS count FROM logs ${genOtelWhere(table, filters)}`
}

// Chart bucketing matches the BigQuery calcChartStart thresholds: minute for
// ranges up to 12h, otherwise hour (the day branch is unreachable, as in BQ).
const otelChartTruncFn = (params: LogsEndpointParams): SafeLogSqlFragment => {
  const ite = params.iso_timestamp_end ? dayjs(params.iso_timestamp_end) : dayjs()
  const its = params.iso_timestamp_start ? dayjs(params.iso_timestamp_start) : dayjs()
  const minuteDiff = ite.diff(its, 'minute')
  const hourDiff = ite.diff(its, 'hour')
  if (minuteDiff > 60 * 12) return safeSql`toStartOfHour`
  if (hourDiff > 24 * 3) return safeSql`toStartOfDay`
  return safeSql`toStartOfMinute`
}

export const genChartQueryOtel = (
  table: LogsTableName,
  params: LogsEndpointParams,
  filters: Filters
): SafeLogSqlFragment => {
  const truncFn = otelChartTruncFn(params)
  const desc = OTEL_SOURCES[table]
  const err = desc.error ?? NO_MATCH
  const warn = desc.warning ?? NO_MATCH
  // The time range is bounded by the endpoint's iso_timestamp_start/end params,
  // so (unlike the BigQuery chart) no explicit timestamp predicate is needed here.
  return safeSql`-- Logs Chart Query (otel) [${lit(table)}]
SELECT
  ${truncFn}(timestamp) AS timestamp,
  countIf(NOT ((${err}) OR (${warn}))) AS ok_count,
  countIf(${err}) AS error_count,
  countIf(${warn}) AS warning_count
FROM logs
${genOtelWhere(table, filters)}
GROUP BY timestamp
ORDER BY timestamp ASC`
}

// Single log fetch by id. id is uuid-shaped (originates from a URL param), so
// reject anything else before interpolating it into the query.
export const genSingleLogQueryOtel = (id: string): SafeLogSqlFragment => {
  if (!/^[0-9a-fA-F-]{1,64}$/.test(id)) {
    throw new Error('Invalid logId')
  }
  return safeSql`-- Single Log Query (otel)
SELECT id, timestamp, event_message, source, severity_text, log_attributes
FROM logs
WHERE id = ${lit(id)}
LIMIT 1`
}

/**
 * Normalizes an OTEL timestamp to the microsecond number the legacy renderers
 * and pagination cursor expect. Shares the parsing heuristic with the unified
 * logs path via `parseOtelTimestamp`.
 */
export const otelTimestampToMicros = (timestamp: unknown): number =>
  parseOtelTimestamp(timestamp).getTime() * 1000

/** Maps an OTEL preview row to the legacy LogData shape (micro-number timestamp). */
export const mapOtelPreviewRow = (row: Record<string, any>): LogData => {
  return { ...row, timestamp: otelTimestampToMicros(row.timestamp) } as LogData
}

/**
 * Reconstructs the nested BigQuery `metadata` shape the api/database single-log
 * detail panels read, from the flat OTEL `log_attributes`. Tables not listed
 * here use a flat row (the default panel reads fields directly).
 *
 * TODO: the OTEL edge logs also carry the JWT/apikey/`sb` subtree as flattened
 * keys (e.g. `request.sb.jwt.apikey.payload.role`), so the api panel's API key /
 * role rows can be reconstructed here in a follow-up.
 */
const SINGLE_LOG_METADATA: Partial<Record<QueryType, (attrs: Record<string, any>) => unknown>> = {
  api: (attrs) => ({
    request: [
      {
        method: attrs['request.method'] ?? null,
        path: attrs['request.path'] ?? null,
        search: attrs['request.search'] ?? null,
        headers: [{ user_agent: attrs['request.headers.user_agent'] ?? null }],
      },
    ],
    response: [
      {
        status_code: attrs['response.status_code'] ?? null,
        // OTEL exposes the gateway error code as `sb_error_code`; BigQuery used
        // `x_sb_error_code`. Coalesce so the panel resolves it either way.
        headers: [
          {
            x_sb_error_code:
              attrs['response.headers.sb_error_code'] ??
              attrs['response.headers.x_sb_error_code'] ??
              null,
          },
        ],
      },
    ],
  }),
  database: (attrs) => ({
    parsed: [
      {
        hint: attrs['parsed.hint'] ?? null,
        detail: attrs['parsed.detail'] ?? null,
        query: attrs['parsed.query'] ?? null,
      },
    ],
  }),
}

/** Maps a single OTEL log row to the legacy LogData shape the detail panel reads. */
export const mapOtelSingleLogToLegacy = (row: OtelLogRow, queryType?: QueryType): LogData => {
  const attrs: Record<string, any> = row.log_attributes ?? {}
  const base = {
    id: row.id,
    timestamp: otelTimestampToMicros(row.timestamp),
    event_message: row.event_message,
  }

  const buildMetadata = queryType ? SINGLE_LOG_METADATA[queryType] : undefined
  if (buildMetadata) {
    return { ...base, metadata: [buildMetadata(attrs)] } as unknown as LogData
  }
  return { ...base, ...attrs } as LogData
}
