import { generateText } from 'ai'
import { source } from 'common-tags'
import { getModel } from 'lib/ai/model'
import apiWrapper from 'lib/api/apiWrapper'
import { NextApiRequest, NextApiResponse } from 'next'
import { logConstants } from 'shared-data'
import { z } from 'zod'

const requestSchema = z.object({
  prompt: z.string().min(1).max(2000),
})

const LOG_TABLES = [
  {
    name: 'edge_logs',
    description: 'Logs obtained from the network edge, containing all API requests',
  },
  { name: 'postgres_logs', description: 'Database logs obtained directly from Postgres' },
  { name: 'function_logs', description: 'Function logs generated from runtime execution' },
  {
    name: 'function_edge_logs',
    description: 'Function call logs, containing the request and response',
  },
  {
    name: 'auth_logs',
    description: 'Errors, warnings, and performance details from the auth service',
  },
  {
    name: 'auth_audit_logs',
    description: 'Audit records of user signups, logins, and account changes',
  },
  {
    name: 'realtime_logs',
    description: 'Realtime server for Postgres logical replication broadcasting',
  },
  { name: 'storage_logs', description: 'Object storage logs' },
  { name: 'postgrest_logs', description: 'RESTful API web server logs' },
  { name: 'supavisor_logs', description: 'Shared connection pooler logs for PostgreSQL' },
  { name: 'pgbouncer_logs', description: 'Dedicated connection pooler for PostgreSQL' },
  {
    name: 'pg_upgrade_logs',
    description: 'Logs generated by the Postgres version upgrade process',
  },
  { name: 'pg_cron_logs', description: 'Postgres logs from pg_cron cron jobs' },
  { name: 'etl_replication_logs', description: 'Logs from the replication process' },
]

async function handler(req: NextApiRequest, res: NextApiResponse) {
  const { method } = req

  switch (method) {
    case 'POST':
      return handlePost(req, res)
    default:
      res.setHeader('Allow', ['POST'])
      res.status(405).json({ data: null, error: { message: `Method ${method} Not Allowed` } })
  }
}

export async function handlePost(req: NextApiRequest, res: NextApiResponse) {
  const parseResult = requestSchema.safeParse(req.body)

  if (!parseResult.success) {
    const errorMessage = parseResult.error.errors.map((e) => e.message).join(', ')
    return res.status(400).json({ error: errorMessage })
  }

  const { prompt } = parseResult.data

  try {
    const {
      model,
      error: modelError,
      providerOptions,
    } = await getModel({
      provider: 'openai',
      routingKey: 'sql',
    })

    if (modelError) {
      return res.status(500).json({ error: modelError.message })
    }

    const result = await generateText({
      model,
      providerOptions,
      system: source`
        You are an expert at writing SQL queries for Supabase Log Explorer.
        The system uses BigQuery-compatible SQL syntax with standard PostgreSQL functions.

        Available log tables:
        ${JSON.stringify(LOG_TABLES, null, 2)}

        Schema fields per table (nested paths require UNNEST joins to access):
        ${JSON.stringify(
          logConstants.schemas.map((s) => ({ table: s.reference, fields: s.fields })),
          null,
          2
        )}

        Key SQL syntax rules:
        - To access nested metadata fields, use LEFT JOIN UNNEST(metadata) AS m ON TRUE
        - For deeper nesting (e.g. request inside metadata): LEFT JOIN UNNEST(m.request) AS req ON TRUE
        - Use cast(timestamp as datetime) for readable timestamps
        - Use regexp_contains(field, r'pattern') for regex matching
        - Always include a LIMIT clause (default to 100 if not specified)
        - If the user doesn't specify a log source, default to edge_logs
        - Return ONLY the SQL query — no markdown code blocks, no explanation, no comments
      `,
      prompt,
    })

    const sql = result.text.trim()

    // Strip any markdown code blocks the model may have added
    const cleanedSql = sql
      .replace(/^```(?:sql)?\n?/i, '')
      .replace(/\n?```$/i, '')
      .trim()

    return res.json({ sql: cleanedSql })
  } catch (error) {
    if (error instanceof Error) {
      console.error(`AI log query generation failed: ${error.message}`)

      if (error.message.includes('context_length') || error.message.includes('too long')) {
        return res.status(400).json({
          error:
            'Your query prompt is too large for Supabase Assistant to ingest. Try a shorter description.',
        })
      }
    } else {
      console.error(`Unknown error: ${error}`)
    }

    return res.status(500).json({
      error: 'There was an unknown error generating the log query. Please try again.',
    })
  }
}

const wrapper = (req: NextApiRequest, res: NextApiResponse) =>
  apiWrapper(req, res, handler, { withAuth: true })

export default wrapper
