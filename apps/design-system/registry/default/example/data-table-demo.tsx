type EmailSort = 'email:asc' | 'email:desc'

export default function DataTableDemo() {
  const [sort, setSort] = React.useState<EmailSort | ''>('')
  const [columnFilters, setColumnFilters] = React.useState<ColumnFiltersState>(
    []
  )
  const [columnVisibility, setColumnVisibility] =
    React.useState<VisibilityState>({})
  const [rowSelection, setRowSelection] = React.useState({})

  const handleSortChange = (column: string) => {
    const [currentCol, currentOrder] = sort.split(':') as [string, 'asc' | 'desc' | '']
    if (currentCol === column) {
      // Cycle through: asc -> desc -> no sort
      if (currentOrder === 'asc') {
        setSort(`${column}:desc` as EmailSort)
      } else {
        setSort('')
      }
    } else {
      // New column, start with asc
      setSort(`${column}:asc` as EmailSort)
    }
  }

  const sortedData = React.useMemo(() => {
    if (!sort) return data
    
    const [sortCol, sortOrder] = sort.split(':') as ['email', 'asc' | 'desc']
    const orderMultiplier = sortOrder === 'asc' ? 1 : -1

    return [...data].sort((a, b) => {
      if (sortCol === 'email') {
        return a.email.localeCompare(b.email) * orderMultiplier
      }
      return 0
    })
  }, [sort])

  const table = useReactTable({
    data: sortedData,
    columns,
    onColumnFiltersChange: setColumnFilters,
    getCoreRowModel: getCoreRowModel(),
    getPaginationRowModel: getPaginationRowModel(),
    getFilteredRowModel: getFilteredRowModel(),
    onColumnVisibilityChange: setColumnVisibility,
    onRowSelectionChange: setRowSelection,
    state: {
      columnFilters,
      columnVisibility,
      rowSelection,
    },
  })

  return (
    <div className="w-full">
      <div className="flex items-center py-4">
        <Input
          size="tiny"
          placeholder="Filter by email"
          value={(table.getColumn("email")?.getFilterValue() as string) ?? ""}
          onChange={(event) =>
            table.getColumn("email")?.setFilterValue(event.target.value)
          }
          className="max-w-sm"
        />
        <DropdownMenu>
          <DropdownMenuTrigger asChild>
            <Button type="default" className="ml-auto" size="tiny" iconRight={<ChevronDown />}>
              Columns
            </Button>
          </DropdownMenuTrigger>
          <DropdownMenuContent align="end" className="max-w-32">
            {table
              .getAllColumns()
              .filter((column) => column.getCanHide())
              .map((column) => {
                return (
                  <DropdownMenuCheckboxItem
                    key={column.id}
                    className="capitalize"
                    checked={column.getIsVisible()}
                    onCheckedChange={(value) =>
                      column.toggleVisibility(!!value)
                    }
                  >
                    {column.id}
                  </DropdownMenuCheckboxItem>
                )
              })}
          </DropdownMenuContent>
        </DropdownMenu>
      </div>
      <div className="overflow-hidden rounded-md border">
        <Table>
          <TableHeader>
            {table.getHeaderGroups().map((headerGroup) => (
              <TableRow key={headerGroup.id}>
                {headerGroup.headers.map((header) => {
                  const isEmailColumn = header.column.id === 'email'
                  return (
                    <TableHead key={header.id}>
                      {header.isPlaceholder
                        ? null
                        : isEmailColumn ? (
                            <TableHeadSort
                              column="email"
                              currentSort={sort}
                              onSortChange={handleSortChange}
                            >
                              Email
                            </TableHeadSort>
                          ) : (
                            flexRender(
                              header.column.columnDef.header,
                              header.getContext()
                            )
                          )}
                    </TableHead>
                  )
                })}
              </TableRow>
            ))}
          </TableHeader>
          <TableBody>
            {table.getRowModel().rows?.length ? (
              table.getRowModel().rows.map((row) => (
                <TableRow
                  key={row.id}
                  data-state={row.getIsSelected() && "selected"}
                  // className="bg-surface-75"
                >
                  {row.getVisibleCells().map((cell) => (
                    <TableCell
                      key={cell.id}
                      className={
                        cell.column.id === 'email'
                          ? 'text-foreground-lighter'
                          : cell.column.id === 'amount'
                            ? 'font-mono'
                            : undefined
                      }
                    >
                      {flexRender(
                        cell.column.columnDef.cell,
                        cell.getContext()
                      )}
                    </TableCell>
                  ))}
                </TableRow>
              ))
            ) : (
              <TableRow>
                
                <TableCell colSpan={columns.length}>
              <p className="text-sm text-foreground">No results found</p>
              <p className="text-sm text-foreground-lighter">
                Your search did not return any results
              </p>
            </TableCell>
              </TableRow>
            )}
          </TableBody>
        </Table>
      </div>
      <div className="flex items-center justify-end space-x-2 py-4">
        <div className="text-foreground-muted flex-1 text-xs">
          {table.getFilteredSelectedRowModel().rows.length} of{" "}
          {table.getFilteredRowModel().rows.length} row(s) selected
        </div>
        <div className="space-x-2">
          <Button
            type="default"
            size="tiny"
            onClick={() => table.previousPage()}
            disabled={!table.getCanPreviousPage()}
          >
            Previous
          </Button>
          <Button
            type="default"
            size="tiny"
            onClick={() => table.nextPage()}
            disabled={!table.getCanNextPage()}
          >
            Next
          </Button>
        </div>
      </div>
    </div>
  )
}
